public class Risorsa {

    private String tipoRisorsa;

    public Risorsa(String tipoRisorsa) {
        this.tipoRisorsa = tipoRisorsa;
    }

    //SETTER
    public void setTipoRisorsa(String tipoRisorsa) {
        this.tipoRisorsa = tipoRisorsa;
    }
    //GETTER
    public String getTipoRisorsa() {
        return tipoRisorsa;
    }
}
